#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	int		pos;

	if (!s)
		return ;
	pos = 0;
	while (s[pos])
		write(fd, &s[pos++], 1);
	write(fd, "\n", 1);
}
